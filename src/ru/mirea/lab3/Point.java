package ru.mirea.lab3;

public class Point {
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public Point(double X, double Y)
    {
        this.x = X;
        this.y = Y;
    }
}
